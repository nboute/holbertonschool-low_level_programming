#include "3-calc.h"

/**
 * op_add - Calculates addition between two numbers
 * @a: number 1
 * @b: number 2
 * Return: Result of operation
 */
int		op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Calculates substitution between two numbers
 * @a: number 1
 * @b: number 2
 * Return: Result of operation
 */
int		op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Calculates multiplication between two numbers
 * @a: number 1
 * @b: number 2
 * Return: Result of operation
 */
int		op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Calculates division between two numbers
 * @a: number 1
 * @b: number 2
 * Return: Result of operation
 */
int		op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_modulo - Calculates modulo between two numbers
 * @a: number 1
 * @b: number 2
 * Return: Result of operation
 */
int		op_mod(int a, int b)
{
	return (a % b);
}
